                                </p>

                                <p class="mb-0"><strong>2. Perhatian Khusus pada Gizi Ibu Hamil</strong></p>
                                <p>Gizi ibu hamil memiliki dampak besar terhadap pertumbuhan janin. Kekurangan gizi pada ibu dapat mengakibatkan stunting pada anak. Oleh karena itu, penting bagi ibu hamil untuk mengonsumsi makanan bergizi, mengandung vitamin dan mineral esensial seperti zat besi, asam folat, dan kalsium. Kunjungan prenatal rutin ke dokter juga dapat membantu memantau kesehatan ibu dan janin.
                                </p>

                                <p class="mb-0"><strong>3. Pemberian Makanan Pendamping ASI yang Tepat</strong></p>
                                <p>Setelah enam bulan, anak perlu diberikan makanan pendamping ASI yang tepat. Makanan pendamping ini harus kaya akan nutrisi yang diperlukan untuk pertumbuhan dan perkembangan anak. Pemilihan makanan pendamping sebaiknya didasarkan pada prinsip keseimbangan gizi, termasuk sumber protein, vitamin, dan mineral yang cukup.
                                </p>

                                <p class="mb-0"><strong>4. Hygiene dan Sanitasi yang Baik</strong></p>
                                <p>Infeksi yang disebabkan oleh buruknya hygiene dan sanitasi dapat menghambat pertumbuhan anak. Oleh karena itu, penting untuk menjaga kebersihan lingkungan sekitar anak, termasuk air minum, makanan, dan area bermain. Praktik cuci tangan yang baik juga merupakan langkah sederhana namun efektif untuk mencegah penyebaran penyakit.
                                </p>

